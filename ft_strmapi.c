#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	char			*result;

	i = 0;
	if (s == 0 || f == 0)
		return (0);
	while (s[i])
		i++;
	if ((result = (char *)malloc(sizeof(char) * (i + 1))) == 0)
		return (0);
	i = 0;
	while (s[i])
	{
		result[i] = f(i, s[i]);
		i++;
	}
	result[i] = '\0';
	return (result);
}
