#include <string.h>

void	*ft_bzero(void *s, size_t n)
{
	unsigned	i;
	char		*adr;

	i = 0;
	adr = s;
	while (i < n)
	{
		adr[i] = '\0';
		i++;
	}
	return (s);
}
